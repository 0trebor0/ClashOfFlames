Note: This method can also be used to create new accounts in old iDevices where it is disabled.</p>
Prerequisites:</p><p>1. Jailbroken Devices</p><p>2. iFile installed. Go to Cydia --&gt; Search for iFile --&gt; Install the iFile from BigBoss
<p>
<img alt="1.jpg" src="http://www.xmodgames.com/images/upload/20160427/1461728414143888.jpg" title="1461728414143888.jpg">
<p>
Now follow these steps:</p><p>1.Open iFile, find the file in the following path "/var/Keychains/keychain-2.db". Tap on the file.
<p>
<img src="http://www.xmodgames.com/images/upload/20160504/1462362871484278.jpg" title="1462362871484278.jpg" alt="1.jpg">
<p>
2. Select SQLite3 Editor. Click on the SQL button at the top left corner.
<p>
<img src="http://www.xmodgames.com/images/upload/20160504/1462362878110796.jpg" title="1462362878110796.jpg" alt="2.jpg">
<p>
<img src="http://www.xmodgames.com/images/upload/20160504/1462362886106447.jpg" title="1462362886106447.jpg" alt="3.jpg">
<p>
<span>3.Enter the following code in SQL statement, respectively</span>
<p><img src="http://www.xmodgames.com/images/upload/20160504/1462362893702061.jpg" title="1462362893702061.jpg" alt="4.jpg">
<p>
delete from genp where agrp like '%supercell.magic'&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (This one is for COC)</p>
<p>delete from genp where agrp like '%clashofthrones.magic'&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(This one is for COC)</p>
<p>delete from genp where agrp like '%supercell.scroll'&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (This one is for COC)
<p>delete from genp where agrp like '%clashofthrones.scroll'&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (This one is for COC)
<p>
<img src="http://www.xmodgames.com/images/upload/20160504/1462362899924046.jpg" title="1462362899924046.jpg" alt="5.jpg">
<p>
Then tap on "Search". The code will be executed automatically. If everything goes well, it'll be like this.
<p>
<img src="http://www.xmodgames.com/images/upload/20160504/1462362905764812.jpg" title="1462362905764812.jpg" alt="6.jpg">
<p>
6. Reboot your iDevice.</p><p>7. Change your gamecenter log-in account</p><p>8. The game will start over in beginner village. You can change or bind new account now.
